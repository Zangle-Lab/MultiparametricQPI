function [row, col] = mapLocation(Loc)
% function to translate a location number (1-864) into the coordinates
% where that location appears in the drug ID table. This was designed for
% use with a 96 well plate with 9 locations in each well. Should the 
% experiment be redesigned in some significant way, this function 
% will also need to be redesigned.
%
% This code 'mapLocation.m' is copyright 2021
% Authors: Edward R. Polanco, Tarek E. Mustafa, Thomas A. Zangle
% This code is distributed under a creative commons attributable
% sharealike license. This license allows you to remix, adapt, and build 
% upon this work, as long as the authors are credited and the modified code
% is redistributed under the same license.

temp = [
        1, 2, 3;
        4, 5, 6;
        7, 8, 9
        ];

locMap = ... 
        [ ...
        temp+9*(1-1),temp+9*(16-1),temp+9*(17-1),temp+9*(32-1),temp+9*(33-1),temp+9*(48-1), ...
            temp+9*(49-1),temp+9*(64-1),temp+9*(65-1),temp+9*(80-1),temp+9*(81-1),temp+9*(96-1); ...
        temp+9*(2-1),temp+9*(15-1),temp+9*(18-1),temp+9*(31-1),temp+9*(34-1),temp+9*(47-1), ...
            temp+9*(50-1),temp+9*(63-1),temp+9*(66-1),temp+9*(79-1),temp+9*(82-1),temp+9*(95-1); ...
        temp+9*(3-1),temp+9*(14-1),temp+9*(19-1),temp+9*(30-1),temp+9*(35-1),temp+9*(46-1), ...
            temp+9*(51-1),temp+9*(62-1),temp+9*(67-1),temp+9*(78-1),temp+9*(83-1),temp+9*(94-1); ...
        temp+9*(4-1),temp+9*(13-1),temp+9*(20-1),temp+9*(29-1),temp+9*(36-1),temp+9*(45-1), ...
            temp+9*(52-1),temp+9*(61-1),temp+9*(68-1),temp+9*(77-1),temp+9*(84-1),temp+9*(93-1); ...
        temp+9*(5-1),temp+9*(12-1),temp+9*(21-1),temp+9*(28-1),temp+9*(37-1),temp+9*(44-1), ...
            temp+9*(53-1),temp+9*(60-1),temp+9*(69-1),temp+9*(76-1),temp+9*(85-1),temp+9*(92-1); ...
        temp+9*(6-1),temp+9*(11-1),temp+9*(22-1),temp+9*(27-1),temp+9*(38-1),temp+9*(43-1), ...
            temp+9*(54-1),temp+9*(59-1),temp+9*(70-1),temp+9*(75-1),temp+9*(86-1),temp+9*(91-1); ...
        temp+9*(7-1),temp+9*(10-1),temp+9*(23-1),temp+9*(26-1),temp+9*(39-1),temp+9*(42-1), ...
            temp+9*(55-1),temp+9*(58-1),temp+9*(71-1),temp+9*(74-1),temp+9*(87-1),temp+9*(90-1); ...
        temp+9*(8-1),temp+9*(9-1),temp+9*(24-1),temp+9*(25-1),temp+9*(40-1),temp+9*(41-1), ...
            temp+9*(56-1),temp+9*(57-1),temp+9*(72-1),temp+9*(73-1),temp+9*(88-1),temp+9*(89-1); ...

        ];

indexMap = locMap == Loc;

[row, col] = find(indexMap);
end